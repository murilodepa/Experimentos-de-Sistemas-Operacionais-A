/* Carga para simular tempo de execução do programa e anotar nas planilhas */

/* Includes Necessarios */
#include <stdio.h>

/* Programa Principal. Contem o processo repetitivo (for) que será a carga */
int main()
{
    long long int count = 0;

    while (1)
    {
        count++;
    }

    return 0;
}
